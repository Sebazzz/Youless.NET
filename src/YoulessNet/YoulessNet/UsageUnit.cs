namespace YoulessNet {
    /// <summary>
    /// Enumerates the units of measurement
    /// </summary>
    public enum UsageUnit {
        /// <summary>
        /// Unknown unit type
        /// </summary>
        Unknown = 0,

        /// <summary>
        /// The units are measured in kilo watt hour (kWh)
        /// </summary>
        KilowattHour,

        /// <summary>
        /// The units are measured in watt
        /// </summary>
        Watt
    }
}